// ── Load signing properties from key.properties (gitignored) ─────────────────
//
// The file `android/key.properties` is NOT committed to git. It contains the
// upload keystore passwords and alias. Locally you create it by hand; in CI
// it is written by the GitHub Actions workflow from repository secrets.
//
// If the file is missing (e.g. a contributor cloning the repo without the
// secrets), we fall back to the debug signing key so `flutter run` still
// works — but release builds will be unsigned and Play will reject them.

val keystoreProperties = Properties()
val keystorePropertiesFile = rootProject.file("key.properties")
val hasKeystore = keystorePropertiesFile.exists()
if (hasKeystore) {
    keystoreProperties.load(FileInputStream(keystorePropertiesFile))
}

android {
    namespace = "be.radioapollo.app"
    compileSdk = flutter.compileSdkVersion
    ndkVersion = flutter.ndkVersion

    compileOptions {
        isCoreLibraryDesugaringEnabled = true
        sourceCompatibility = JavaVersion.VERSION_17
        targetCompatibility = JavaVersion.VERSION_17
    }

    kotlinOptions {
        jvmTarget = JavaVersion.VERSION_17.toString()
    }

    defaultConfig {
        applicationId = "be.radioapollo.app"
        minSdk = flutter.minSdkVersion
        targetSdk = flutter.targetSdkVersion
        versionCode = flutter.versionCode
        versionName = flutter.versionName
    }

    signingConfigs {
        if (hasKeystore) {
            create("release") {
                keyAlias = keystoreProperties["keyAlias"] as String
                keyPassword = keystoreProperties["keyPassword"] as String
                storeFile = file(keystoreProperties["storeFile"] as String)
                storePassword = keystoreProperties["storePassword"] as String
            }
        }
    }

    buildTypes {
        release {
            signingConfig = if (hasKeystore) {
                signingConfigs.getByName("release")
            } else {
                // Fallback so `flutter run --release` works without the keystore.
                // Real release builds MUST have key.properties present.
                signingConfigs.getByName("debug")
            }
        }
    }
}

